No1 = int(input("Enter 1st number : "))
No2 = int(input("Enter 2nd number : "))

def main():

    print(f"Addition is : {No1 + No2}")
    print(f"Subtraction is : {No1 - No2}")
    print(f"Multiplication is : {No1 * No2}")
    print(f"Division is : {No1 / No2}")

if __name__ == "__main__":
    main()